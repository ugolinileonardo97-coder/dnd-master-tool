export function getRegionalTavernSideQuests(regionId, level) {
  const pool =
    tavernRegionalSideQuests[regionId] || tavernRegionalSideQuests.generic;
  const numericLevel = Number(level) || 1;
  const matching = pool.filter(
    (entry) => numericLevel >= entry.minLevel && numericLevel <= entry.maxLevel
  );

  return matching.length ? matching : pool;
}
