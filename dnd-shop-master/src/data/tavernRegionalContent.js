function dish(name, description, index) {
  return {
    name,
    description,
    bonus: dishBonuses[index % dishBonuses.length],
    malus: dishMaluses[index % dishMaluses.length],
  };
}

function quest(summary, description, reward, complication, minLevel, maxLevel) {
  return {
    summary,
    description,
    reward,
    complication,
    minLevel,
    maxLevel,
  };
}

